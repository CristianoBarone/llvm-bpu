#ifndef LLVM_LIB_TARGET_BPU_INSTPRINTER_BPUINSTPRINTER_H
#define LLVM_LIB_TARGET_BPU_INSTPRINTER_BPUINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class BPUInstPrinter : public MCInstPrinter {
public:
  BPUInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                  const MCRegisterInfo &MRI);

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);

  void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

  bool printAliasInstr(const MCInst *MI, raw_ostream &OS);
  void printCustomAliasOperand(const MCInst *MI, unsigned OpIdx,
                               unsigned PrintMethodIdx, raw_ostream &O);

private:
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
};
} // end namespace llvm

#endif // end LLVM_LIB_TARGET_BPU_INSTPRINTER_BPUINSTPRINTER_H
